package com.app.model;

public class PolicyAlterationKeyAndValue {
	
	private String key;
	private String value;
	public PolicyAlterationKeyAndValue() {
		key = "";
		value="";
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}

}
